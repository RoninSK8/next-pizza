'use server';

import { prisma } from '@/prisma/prisma-client';
import { PayOrderTemplate } from '@/shared/components/shared';
import { CheckoutFormValues } from '@/shared/constants';
import { createPayment, getUserSession, sendEmail } from '@/shared/lib';
import { OrderStatus, Prisma } from '@prisma/client';
import { hashSync } from 'bcryptjs';
import { cookies } from 'next/headers';
import { UserVerificattionTemplate } from '@/shared/components/shared/email-templates/user-verificattion';

export async function createOrder(data: CheckoutFormValues) {
	try {
		const cookieStore = cookies();
		const cartToken = cookieStore.get('cartToken')?.value;
		if (!cartToken) {
			throw new Error('Cart token not found');
		}

		// Находим корзину по токену
		const userCart = await prisma.cart.findFirst({
			include: {
				user: true,
				items: {
					include: {
						ingredients: true,
						productItem: {
							include: {
								product: true,
							},
						},
					},
				},
			},
			where: {
				token: cartToken,
			},
		});

		// Проверка на наличие корзины
		if (!userCart) {
			throw new Error('Cart not found');
		}

		// Проверка на наличие продуктов
		if (userCart?.totalAmount === 0) {
			throw new Error('Cart is empty');
		}

		// Создание заказа
		const order = await prisma.order.create({
			data: {
				token: cartToken,
				fullName: data.firstName + ' ' + data.lastName,
				email: data.email,
				phone: data.phone,
				address: data.address,
				comment: data.comment,
				totalAmount: userCart.totalAmount,
				status: OrderStatus.PENDING,
				items: JSON.stringify(userCart.items),
			},
		});

		// Очистка корзины
		await prisma.cart.update({
			where: {
				id: userCart.id,
			},
			data: {
				totalAmount: 0,
			},
		});

		await prisma.cartItem.deleteMany({
			where: {
				cartId: userCart.id,
			},
		});

		// Создание платежа
		const paymentData = await createPayment({
			amount: order.totalAmount,
			description: 'Оплатаа заказа #' + order.id,
			orderId: order.id,
		});

		// Проверка на создание платежа
		if (!paymentData) {
			throw new Error('Payment data not found');
		}

		// Обновление статуса заказа
		await prisma.order.update({
			where: {
				id: order.id,
			},
			data: {
				paymentId: paymentData.id,
			},
		});

		const paymentUrl = paymentData.confirmation.confirmation_url;
		// Отправка письма

		await sendEmail(
			data.email,
			'Next Pizza / Оплатите заказ №' + order.id,
			PayOrderTemplate({
				orderId: order.id,
				totalAmount: order.totalAmount,
				paymentUrl,
			})
		);

		return paymentUrl;
	} catch (error) {
		console.log('[ORDER_CREATE] Server error', error);
	}
}

export async function updateUserInfo(body: Prisma.UserUpdateInput) {
	try {
		const currentUser = await getUserSession();
		if (!currentUser) {
			throw new Error('Пользователь не найден');
		}
		const findUser = await prisma.user.findFirst({
			where: {
				id: Number(currentUser.id),
			},
		});

		await prisma.user.update({
			where: {
				id: Number(currentUser.id),
			},
			data: {
				fullName: body.fullName,
				email: body.email,
				password: body.password
					? hashSync(body.password as string, 10)
					: findUser?.password,
			},
		});
	} catch (error) {
		console.log('Error [USER_UPDATE]', error);
		throw error;
	}
}

export async function registerUser(body: Prisma.UserCreateInput) {
	try {
		const user = await prisma.user.findFirst({
			where: {
				email: body.email,
			},
		});

		if (user) {
			if (!user.verified) {
				throw new Error('Почта не подтверждена');
			}
			throw new Error('Пользователь уже существует');
		}

		const createdUser = await prisma.user.create({
			data: {
				fullName: body.fullName,
				email: body.email,
				password: hashSync(body.password as string, 10),
			},
		});

		const code = Math.floor(100000 + Math.random() * 900000).toString();
		await prisma.verificationCode.create({
			data: {
				code,
				userId: createdUser.id,
			},
		});

		await sendEmail(
			body.email,
			'Next Pizza / Подтверждение регистрации',
			UserVerificattionTemplate({
				code,
			})
		);
	} catch (error) {
		console.log('Error [USER_CREATE]', error);
		throw error;
	}
}
